package week.day1;

public class CheckIfTheNumberIsPositiveOrNegative {
	
public static void main(String[] args) {
	int number = 56;

	if(number>0)
	{
		System.out.println("The Number is Positive: " + number);
	}

	else if (number<0)
	{
		System.out.println("The Number is Negative: " + number);
	}
	else
	{
		System.out.println("The Number is Negative: " + number);
	}
}
	
}
